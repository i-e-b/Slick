namespace SlickCommon.Canvas
{
    /// <summary>
    /// A rectangle by another name
    /// </summary>
    public class Quad
    {
        public int X;
        public int Y;
        public int Width;
        public int Height;

        public Quad(int x, int y, int width, int height) { X = x; Y = y; Width = width; Height = height; }
    }
}